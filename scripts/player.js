function mmssuration(duration){
    let minutes = Math.floor(duration / 60);
    let seconds = duration % 60;
    if (minutes < 10) {
      minutes = "0" + minutes;
    }
    if (seconds < 10) {
      seconds = "0" + seconds;
    }
    return minutes.toString() + ':' + seconds.toString();
   }

